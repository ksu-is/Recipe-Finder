# Prompt the user for their meal choice
meal_type = input("What meal would you like to make? (breakfast, lunch, dinner): ").lower()

# Check if meal type is valid
if meal_type not in recipes:
    print("Invalid meal type selected.")
else:
    ingredients = []
    print("Please enter the ingredients you have on hand (type 'exit' to stop input).")

    # User inputs ingredients they have
    ingredientInput = ""
    loopCounter = 1
    while ingredientInput != "exit":
        ingredientInput = input(f"Ingredient # {loopCounter}: ")
        if ingredientInput == "exit":
            break
        ingredients.append(ingredientInput.lower())
        loopCounter += 1

    print(f"Ingredients entered: {ingredients}")

    # Find recipes that can be made with the ingredients on hand
    possible_recipes = []
    for recipe in recipes[meal_type]:
        match_count = sum(1 for ingredient in recipe['ingredients'] if ingredient in ingredients)
        if match_count > 0:
            possible_recipes.append(recipe)

    # Display recipes that can be made
    if possible_recipes:
        print(f"\nHere are the {meal_type} recipes you can make:")
        for idx, recipe in enumerate(possible_recipes, 1):
            print(f"{idx}. {recipe['name']}")

        # Ask user to select a recipe
        selected_recipe_num = input("\nPlease enter the number of the recipe you'd like to make: ")

        # Validate input
        try:
            selected_recipe_num = int(selected_recipe_num)
            if selected_recipe_num < 1 or selected_recipe_num > len(possible_recipes):
                print("Invalid selection.")
            else:
                selected_recipe = possible_recipes[selected_recipe_num - 1]
                print(f"\nYou have selected {selected_recipe['name']}.")

                # Create shopping list for missing ingredients
                missing_ingredients = [ingredient for ingredient in selected_recipe['ingredients'] if ingredient not in ingredients]

                print(f"\nShopping list for missing ingredients: {', '.join(missing_ingredients)}")

                # Display recipe instructions
                print(f"\nHere are the instructions to make {selected_recipe['name']}:")
                print(selected_recipe['instructions'])
        except ValueError:
            print("Please enter a valid number.")
    else:
        print("There is not a recipe that contains those ingredients.")






